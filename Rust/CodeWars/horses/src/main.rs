fn riders(stations: &Vec<u32>) -> u32 {
	let mut riders: u32 = 0;
	let mut miles: u32 = 0;

	if stations.len() == 0 {
		return 0
	}

	for s in stations {
		miles += *s;

		if miles > 100 {
			miles = *s;
			riders += 1;
		}
	}

	if miles > 100 {
		return riders + 2
	}

	riders + 1
}

